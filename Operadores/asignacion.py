# Operdaores de Asignaciṕn:

# operador igual para asignar variables
variable = 10
print(f'variable: {variable}')

# reasignación
variable = variable + 1  # 11 
print(f'variable reasignada: {variable}')

# incremento con reasignación(válido con cualquier operador arimético)
variable += 1  # 12
print(f'Variable reasignada incrementada: {variable}')
