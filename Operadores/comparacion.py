# Operadores de Comparación

primer_variable = 4
segunda_variable = 3

# Comparación de igual/igual:
resultado = primer_variable == segunda_variable
print(f'v1={primer_variable} \n v2={segunda_variable} \n son ==? {resultado}')

# Comparación desigual/igual:
resultado_2 = primer_variable != segunda_variable
print(f'v1={primer_variable} \n v2={segunda_variable} \n son !=? {resultado_2}')

# Comparación mayor que:
resultado_2 = primer_variable > segunda_variable
print(f'v1={primer_variable} es > que v2={segunda_variable}? {resultado_2}')

# Comparación mayor/igual que:
resultado_2 = primer_variable >= segunda_variable
print(f'v1={primer_variable} es >= que v2={segunda_variable}? {resultado_2}')

# Comparación menor que:
resultado_2 = primer_variable < segunda_variable
print(f'v1={primer_variable} es < que v2={segunda_variable}? {resultado_2}')

# Comparación menor/igual que:
resultado_2 = primer_variable <= segunda_variable
print(f'v1={primer_variable} es <= que v2={segunda_variable}? {resultado_2}')
