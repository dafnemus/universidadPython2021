# Condicionales IF - ELSE

condicion = True

# mientras se reciba un valor distinto de vacio se devolvera TRUE

# if/else basico
if condicion:
    print('Condicion es verdadera')
else:
    print('Condicion falsa')

# if/elif/else
palabra = False

if palabra == True:
    print('Condición verdadera')
elif palabra == False:
    print('Condicion Falsa')
else:
    print('Condición no reconocida')
